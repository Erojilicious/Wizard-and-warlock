public class Wizard extends Src {

    private int HitPoints = 3500;
    private int ManaPoints = 1200;
    private int RayOfLight = 300;

    public int getHitPoints() {
        return HitPoints;
    }
    public void setHitPoints(int hitPoints) {
        HitPoints = hitPoints;
    }
    public int getManaPoints() {
        return ManaPoints;
    }
    public void setManaPoints(int manaPoints) {
        ManaPoints = manaPoints;
    }
    public int getRayOfLight() {
        return RayOfLight;
    }
    public void setRayOfLight(int rayOfLight) {
        RayOfLight = rayOfLight;
    }

}